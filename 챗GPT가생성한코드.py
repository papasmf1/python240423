# 리스트(List) 생성
my_list = [1, 2, 3, 4, 5]

# 튜플(Tuple) 생성
my_tuple = (1, 2, 3, 4, 5)

# 세트(Set) 생성
my_set = {1, 2, 3, 4, 5}

# 딕셔너리(Dictionary) 생성
my_dict = {'a': 1, 'b': 2, 'c': 3, 'd': 4, 'e': 5}

# 각 자료형의 특징 비교
print("리스트(List)의 특징:")
print("- 인덱싱과 슬라이싱 가능")
print("- 값 변경 가능")
print("- 중복된 값 허용")
print("- 순서가 유지됨\n")

print("튜플(Tuple)의 특징:")
print("- 인덱싱과 슬라이싱 가능")
print("- 값 변경 불가능 (Immutable)")
print("- 중복된 값 허용")
print("- 순서가 유지됨\n")

print("세트(Set)의 특징:")
print("- 인덱싱과 슬라이싱 불가능")
print("- 값 변경 가능 (mutable)")
print("- 중복된 값은 허용되지 않음")
print("- 순서가 유지되지 않음 (Unordered)\n")

print("딕셔너리(Dictionary)의 특징:")
print("- 키-값 쌍으로 구성")
print("- 키를 사용하여 값에 접근")
print("- 키는 중복되지 않음")
print("- 순서가 유지되지 않음 (Unordered)")
